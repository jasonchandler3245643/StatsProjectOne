
public class Person {

	// integer value birthday (1/1 = 1, 1/2 = 2, etc)
	private int birthday;
	
	public Person(int day) {
		
		birthday = day;
		
	}
	
	
	public int getBirthday() {
		
		return birthday;
		
	} 
	
	public void setBirthday(int day) {
		
		birthday = day;
		
	}
}
